# This script calculates the simple interest based on a given principal amount,
# annual interest rate, and time.

# Define the variables for principal, rate, and time
principal = 1000  # P: The principal amount (initial investment)
rate = 0.05       # R: The annual interest rate (as a decimal, 5% = 0.05)
time = 3          # T: The time the money is invested for in years

# Calculate the simple interest using the formula: I = P * R * T
# I is the interest earned
interest = principal * rate * time

# Print the calculated interest in the specified format
print(f"The simple interest is: {interest}")

